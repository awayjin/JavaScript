HW.Country = (function() {
    var myScroller;
    /**
     *  生成国家列表项函数
     * @param groupKey    分组Id
     * @param index       分组编号
     * @param item        分组项
     * @returns {StringBuffer}
     * @private
     */
    var _makeCountryItem = function (groupKey, index, item) {
        var itemHtml = new StringBuffer();
        var itemId = "span_" + groupKey + "_" + index;
        var info = $.toJSON(item);
        itemHtml.append('<dd><span id=');
        itemHtml.append(itemId);
        itemHtml.append(' country=\''+info+'\'');
        itemHtml.append('>');
        itemHtml.append(item.Country_Name);
        itemHtml.append('</span></dd>');
        return itemHtml;
    };
    /**
     * 生成国家分组表头项函数
     * @param item         列表项数据
     * @returns {StringBuffer}
     * @private
     */
    var _makeGroupItem = function (item) {
        var itemHtml = new StringBuffer();
        var contentHtml = new StringBuffer();
        itemHtml.append('<dt class="list-country_tip_ltt" id=divider_');
        itemHtml.append(item.dataDivider);
        itemHtml.append('>');
        itemHtml.append(item.dataDivider);
        itemHtml.append('</dt>');
        for (var i = 0; i < item.dataList.length; i++) {
            contentHtml += _makeCountryItem(item.dataDivider, i, item.dataList[i]);
        }
        itemHtml.append(contentHtml);
        return itemHtml;
    };
    /**
     *  初始化国家列表
     * @private
     */
    var _initCountryList = function () {
        $('#keysPanel').hide();
        var contentHtml = new StringBuffer(),Country = HW.lang.getCurrentLang();
        for (var i = 0; i < country[Country].length; i++) {
            if (country[Country][i].dataList.length > 0) {
                contentHtml += _makeGroupItem(country[Country][i]);
                $.each($("#keysPanel a"),function(index,keyItem){
                    if(keyItem.text == country[Country][i].dataDivider){
                        $("#"+this.id).addClass("act");
                        $("#"+this.id).unbind("click").bind("click",function(){
                            HW.Country.keyItemClick(this);
                        });
                    }
                });
            }
        }
        $("#countryList").html(contentHtml.toString());

        $("[id*='span_']").unbind("click").bind("click",function(){
                HW.Country.setCurrentCountryInfo(this);
            });

        $("[id*='divider_']").unbind("click").bind("click",function(){
            console.log("菜单选择=============");
            $('#keysPanel').show();
        });
    };
	return {
		//Core中注册对象
		pageConfig : {
			name : "Country",
			id : "country"
        },
		//初始化
		init : function() {
            HW.Tools.myMenuBottom('1');
            $(document).undelegate("#country","pageshow").delegate( "#country", "pageshow",function(){
                console.log("=========country======");


                HW.lang.replaceLocalString();
                myScroller = HW.Tools._addiScroll("countryWrapper","country_outter",0,0,0,0);
                myScroller.refresh();
            });
			_initCountryList();
		},
        //选择国家，并存储国家信息
		setCurrentCountryInfo : function(event){
            var curInfo = $("#"+event.id).attr("country");
			if(window.localStorage){
				window.localStorage.removeItem("CurrentCountry");
				window.localStorage.setItem("CurrentCountry", curInfo);
			}
            $.mobile.changePage("contactHW.html");
            //$.mobile.back();
		},
        //点击关键字，跳转到指定位置
        keyItemClick : function(event){
            var keyItem = $("#"+event.id).text();
            myScroller.scrollToElement($("#divider_"+keyItem)[0],10);
            $('#keysPanel').hide();
        }
	}
})();

HW.Core.addModule("Country", HW.Country);
HW.Core.loadPage("Country");