package com.codegoing;

public class Teacher {
  private int age;
  private String name;

  public Teacher() {
  }

  public Teacher(int age, String name) {
    this.age = age;
    this.name = name;
  }

  public int getAge() {
    return age;
  }

  public void setAge(int age) {
    this.age = age;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }
}
